	<h5>General</h5>

	<div class="question-wrapper well">
		<div class="question">
			Q: What is RFP-EZ?
		</div>
		<div class="answer">
			A: RFP-EZ is a web-based application that is comprised of five different systems, all meant to make it easier for small businesses to sell their services to government buyers, and for contracting officers to buy small dollar value services.
		</div>
	</div>  

	<div class="question-wrapper well">
		<div class="question">
			Q: Are there benefits to buyers and sellers in using RFP-EZ?
		</div>
		<div class="answer">
			A: Yes. We know that buying goods and services for the Government and selling them to the Government can be complicated and intimidating.  That shouldn’t be the case, especially for small dollar value purchases.  RFP-EZ helps streamline the contracting process for both the buyers and sellers.  It represents a unique opportunity for innovative startups to easily access the federal government marketplace and, in turn, enable the Federal government to secure better and less expensive products and services, saving taxpayers money and improving the results delivered. 
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: Is this a government-wide initiative? 
		</div>
		<div class="answer">
			A: Yes. RFP-EZ is a government-wide initiative to help all procurement officials in the Federal government.  It is part of the Presidential Innovation Fellowship Program, which was launched in 2012 under the direction of President Barack Obama and his Administration, to develop solutions for the government that can save lives, save taxpayer money, and fuel job creation. 
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: If an agency uses RFP-EZ for acquisitions over $25,000, must it also post a notice in FBO? If so, why?
		</div>
		<div class="answer">
			<p>A: For procurements valued from $25,001 to $150,000, an agency must synopsize it in FedBizOpps (<a href="//www.fbo.gov">www.fbo.gov</a>).  At this time, the posting can be as simple as a link to the project here on RFP-EZ with the following text:
				<p>
				<blockquote>The solicitation is being issued in accordance with the procedures of FAR Part 13.  This is a competitive requirement for [insert name of services].  This is a [insert set-aside] and has been assigned NAICS code [insert NAICS code] with a corresponding size standard of [insert size standard].  We anticipate issuing a [fixed price, time and material] order.
					<br /><br />
					The request for quotation can be electronically accessed and responded to via the following link: [insert link provided in RFP-EZ]. All changes to the requirement that occur prior to the closing date will be posted in FBO and RFP-EZ. It is the responsibility of vendors to monitor FBO and RFP-EZ for amendments that may be issued. 
				</blockquote>
		  	<p>However, we are working on creating a seemliness link from FBO to RFP-EZ so that when a CO posts an RFP or RFQ in RFP-EZ, it also posts automatically to FBO.  This feature is coming soon!
		  	</p>
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: What happens when an agency amends the SOW/RFP?
		</div>
		<div class="answer">
		  <p>A: If a notice is required to be published on FBO, you would publish the notice of the amendment on FBO, with a link to the amendment on RFP-EZ.  However, we are working on creating a seemliness link from FBO to RFP-EZ so that when a CO posts an amendment to an RFP or RFQ in RFP-EZ, it also posts automatically to FBO.  This feature is coming soon!
		  </p>
		  <p>If a notice is not required to be published on FBO, you would amend the SOW/RFP on RFP-EZ, with the assistance of the administrators of the system.
		  </p>
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: Why is RFP-EZ limited to acquisitions valued below the SAT ($150,000)?
		</div>
		<div class="answer">
			<p>A: Generally, for acquisitions below the SAT, agencies can use simplified procedures for the purchase.  The purpose of these simplified procedures, like the purpose of RFP-EZ, is to:
			</p>
			<ul>
				<li>Reduce administrative costs;</li>
				<li>Improve opportunities for small, small disadvantaged, women-owned, veteran-owned, HUBZone, and service-disabled veteran-owned small businesses;</li>
				<li>Promote efficiency and economy in contracting; and</li>
				<li>Avoid unnecessary burdens for agencies and contractors.</li>
			</ul>
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: What are the parts or components of RFP-EZ?
		</div>
		<div class="answer">
			A: There are five parts: SOWComposer; BidMaker; BidMonitor; RFP-EZ Marketplace; and Portfolio Browser. 
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: What is SOWComposer?
		</div>
		<div class="answer">
			A: SOWComposer is a web-based system that allows contracting officers to post solicitations created by SOWComposer and valued under $150,000 (sometimes referred to as the Simplified Acquisition Threshold or SAT) for certain IT services in a streamlined and standardized format. 
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: What are the benefits of SOWComposer?
		</div>
		<div class="answer">
			<p>A: Many times when a program office wants to buy something, they have to draft a statement of work (SOW) telling the public what exactly the Government is trying to buy.  We thought it would make things easier if these program offices, including program offices in the same agency or different agencies, had a template already in place to help them draft their SOW.  That’s why we created SOWComposer.  SOWComposer lets:
			</p>
			<ul>
				<li> the program offices write clear and easy to understand SOWs or use an already-existing SOW template;</li>
				<li> contracting officers view and edit the SOWs;</li>
				<li> the Government store these SOWs in a system to be retrieved later by any Government user; and</li>
				<li> the contracting officer post the SOW online.</li>
			</ul>
 
			<p>The SOWComposer actually drafts more than just the SOW.  It really drafts the agency’s RFQ/RFP and allows the program office and contracting officer to insert the required deliverables, timelines, clauses, etc.
			</p>
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: What is BidMaker?
		</div>
		<div class="answer">
			<p>A: BidMaker allows small businesses to respond easily to RFQs posted by agencies that used SOWComposer.  Using BidMaker, small businesses can:
			</p>
			<ul>
				<li>respond online instead of having to download the solicitation and respond with a paper-based offer></li>
				<li>ask questions about the acquisition online, and the agency will post the response online  and all offerors can then see the questions and answers in real-time</li>
			 	<li>Access “helper text” to assist in responding to the solicitation</li>
				<li>save their draft proposals for later review and use again</li>
				<li>store some standard information provided in the BidMaker to pre-populate required forms on the current and future acquisitions.</li>
			</ul>
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: What is BidMonitor?
		</div>
		<div class="answer">
			<p>A: BidMonitor helps contracting officers and program offices to organize proposals that are generated by BidMaker. It shows them:
			</p>
			<ul>
				<li>a list of current SOWs composed by SOWComposer and bid upon by BidMaker;</li>
				<li>information from proposals received, such as the name of offeror, date offer was submitted, and summary information from SAM;</li>
				<li>the full text of proposals;</li>
				<li>discussions between the reviewers and contracting officer about the proposals;</li>
				<li>prices previously bid for the same or similar services.</li>
			</ul>
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: What is RFP-EZ Marketplace?
		</div>
		<div class="answer">
			A: Marketplace is a way for small businesses to find out when new SOWs composed on SOWComposer are posted that match the business’ profile or when new IT-related acquisitions (valued above or below $150,000) are posted on FBO.  Once a small business signs up for an account, Marketplace lets them review SOWs, send messages to the agency points of contact, and receive email alerts when new solicitations are posted.
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: What is Portfolio Browser?
		</div>
		<div class="answer">
		  <p>A: This search tool allows agencies to find a list of small businesses that have already performed work for the Government.  The agencies can view a profile of the business, its location, small business status, prior awards and award cost, and current catalog pricing of the business. 
		  </p>
		  <p>
		  This helps small businesses market their services and products to the Government.  It helps the Government find cost information to better prepare its independent Government estimate and helps ensure the Government gets the best price for the products and services it needs. 
		</p>
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: Isn’t Portfolio Browser and SBA’s Small Business Supplemental System (SBSS)/Dynamic Small Business Search (DSBS) the same thing?
		</div>
		<div class="answer">
			A: Not really.  With portfolio browser, the small business can provide much more information about its company, such as prior quotes submitted, sample work, and other information.
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: How can an agency participate in RFP-EZ?
		</div>
		<div class="answer">
			A: On December 28, 2012, the Small Business Administration (the agency sponsoring the initiative) issued a notice announcing RFP-EZ as a pilot program at 77 FR 76588, available <a href="http://www.gpo.gov/fdsys/pkg/FR-2012-12-28/html/2012-31323.htm">here</a>.   If the agency has a web design or development requirement valued below $150,000, it should go to <a href="<?php echo e(route('root')); ?>">our homepage</a> to find out more information about signing on to the system.
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: Are there any special requirements to participate? 
		</div>
		<div class="answer">
			In this stage of the program, we have focused on helping innovative small businesses gain entry into the Federal marketplace and therefore have focused on web design and development services with an estimated price of under $150,000.  In the next stages, RFP-EZ could be open to all businesses and for all services. 
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: If this is a pilot program, does it mean it’s exempt from the FAR?
		</div>
		<div class="answer">
			A: No.  Acquisitions conducted using RFP-EZ are subject to the FAR.  The purpose of RFP-EZ is to make following the FAR requirements easier, both for agencies and small businesses. 
		</div>
	</div>

	<div class="question-wrapper well">
		<div class="question">
			Q: How will you be evaluating RFP-EZ during this initial stage?
		</div>
		<div class="answer">
			A: We will be evaluating RFP-EZ on a number of levels, including whether it increases participation by new entrant small business and reduces time required to develop an SOW. 
		</div>
